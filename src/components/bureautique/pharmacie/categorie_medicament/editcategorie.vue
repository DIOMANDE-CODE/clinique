export default {
  name: "ajoutdepartement",
  data() {
    return {
      success: false,
      errors: false,
      message: "",
      preloader: false,
      nom: "",
      description: "",
    };
  },
  created() {
    this.charger();
  },
  components: {
    loader,
  },
  methods: {
    charger() {
      axios
        .create({
          headers: {
            "Content-Type": "application/json",
            Authorization: "Bearer " + localStorage.getItem("token"),
            "Access-Control-Allow-Origin": "*",
          },
        })
        .get(chemin + "/categorieMedoc/" + this.$route.params.id)
        .then((response) => {
          console.log(response.data);
          this.preloader = false;
          this.nom = response.data.libelle;
          this.description = response.data.specification;
        })
        .catch((err) => {
          this.preloader = false;
          this.errors = true;
          console.log(err);
        });
    },
    renitialiser() {
      (this.nom = ""), (this.description = "");
    },
    modifier() {
      if (this.nom === "") {
        // this.errors = true
        // this.message = "Veuillez saisir le nom du departement"
      } else {
        this.preloader = true;
        var categorie = {
          libelle: this.nom,
          specification: this.description,
        };
        console.log(categorie);
        axios
          .create({
            headers: {
              "Content-Type": "application/json",
              Authorization: "Bearer " + localStorage.getItem("token"),
              "Access-Control-Allow-Origin": "*",
            },
          })
          .put(chemin + "/categorieMedoc/" + this.$route.params.id, categorie)
          .then((response) => {
            console.log(response.data);
            this.preloader = false;
            this.$swal({
              html: "Catégorie modifiée",
              icon: "success",
              confirmButtonText: `OK`,
            }).then((result) => {
              if (result.isConfirmed) {
                this.$router.push("/pharmacie/categorie");
              }
            });
          })
          .catch((err) => {
            this.preloader = false;
            this.errors = true;
            console.log(err);
          });
      }
    },
    retourner() {
      this.$router.push("/pharmacie/categorie");
    },
  },
};
