                zeroRecords: "Aucun enregistrement correspondant trouvé",
                stateSave: true,
                paginate: {
                  first: "Premier",
                  last: "Dernier",
                  next: "Suivant",
                  previous: "Précédent",
                },
                aria: {
                  sortAscending: ": activate to sort column ascending",
                  sortDescending: ": activate to sort column descending",
                },
              },
            });
          }, 1000);
        } else {
          this.preload = false;
          this.message = "Aucun employés existants";
          console.log("erreur de chargement");
        }
      });
  },
  data() {
    return {
      profils: [],
      identifiant: null,
      status: "",
      preload: false,
      success: false,
      errors: false,
      message: "",
    };
  },
  components: {
    loader,
  },
  methods: {
    charge() {
      this.preload = true;
      axios
        .create({
          headers: {
            "Content-Type": "application/json",
            Authorization: "Bearer " + localStorage.getItem("token"),
            "Access-Control-Allow-Origin": "*",
          },
        })
        .get(chemin + "/getListeProfile")
        .then((response) => {
          console.log(" getListeProfile :", response.data.data);
          if (response.data.state === true) {
            this.preload = false;
            this.profils = response.data.data;
          } else {
            this.preload = false;
            this.message = "Aucun employés existants";
            console.log("erreur de chargement");
          }
        });
    },
    modifier(pk) {
      this.$router.push("/profil/edit/" + pk);
    },
    supprimer(pk) {
      this.preload = true;
      axios
        .create({
          headers: {
            "Content-Type": "application/json",
            Authorization: "Bearer " + localStorage.getItem("token"),
            "Access-Control-Allow-Origin": "*",
          },
        })
        .delete(chemin + "/deleteProfil/" + pk)
        .then(() => {
          this.preload = false;
          this.$swal({
            html: "Profil supprimé",
            icon: "success",
            confirmButtonText: `OK`,
          }).then((result) => {
            if (result.isConfirmed) {
              this.$router.push("/admin/profil");
            }
          });
        })
        .catch((err) => {
          this.preload = false;
          console.log(err);
        });
    },
    activer(pk) {
      this.preload = true;
      axios
        .create({
          headers: {
            "Content-Type": "application/json",
            Authorization: "Bearer " + localStorage.getItem("token"),
            "Access-Control-Allow-Origin": "*",
          },
        })
        .post(chemin + "/activerDesactiverProfile/", {
          id: pk,
          statut: "actif",
        })
        .then((response) => {
          if (response.data.state === true) {
            this.preload = false;
            this.success = true;
            this.message = response.data.message;
            this.charge();
          } else {
            this.preload = false;
            this.errors = true;
            this.message = response.data.message;
          }
        })
        .catch((err) => {
          this.preload = false;
          console.log(err);
        });
    },
  },
};
